// ============================================
// SPRITE CACHE - Pre-render pour la performance
// Chaque sprite est rendu une seule fois sur un
// canvas offscreen, puis blit avec drawImage
// ============================================

const _spriteCache = new Map();

function _getSpriteKey(spriteData) {
    // Use object identity as cache key
    if (!spriteData._cacheId) {
        spriteData._cacheId = '_sp_' + (++_getSpriteKey._counter);
    }
    return spriteData._cacheId;
}
_getSpriteKey._counter = 0;

function _renderSpriteToCanvas(spriteData, scale) {
    const rows = spriteData.length;
    const cols = spriteData[0].length;
    const canvas = document.createElement('canvas');
    canvas.width = cols * scale;
    canvas.height = rows * scale;
    const ctx = canvas.getContext('2d');

    for (let row = 0; row < rows; row++) {
        for (let col = 0; col < cols; col++) {
            const colorIndex = spriteData[row][col];
            if (colorIndex === 0) continue;
            const color = PALETTE[colorIndex];
            if (!color || color === 'transparent') continue;
            ctx.fillStyle = color;
            ctx.fillRect(col * scale, row * scale, scale, scale);
        }
    }
    return canvas;
}

function getCachedSprite(spriteData, scale = 2) {
    const key = _getSpriteKey(spriteData) + '@' + scale;
    let cached = _spriteCache.get(key);
    if (!cached) {
        cached = _renderSpriteToCanvas(spriteData, scale);
        _spriteCache.set(key, cached);
    }
    return cached;
}

// Fonction utilitaire pour dessiner un sprite (OPTIMISEE)
function drawSprite(ctx, spriteData, x, y, scale = 2) {
    const cached = getCachedSprite(spriteData, scale);
    ctx.drawImage(cached, Math.floor(x), Math.floor(y));
}

// Dessiner un tile
function drawTile(ctx, tileType, x, y, scale = 2) {
    const sprite = TILE_SPRITES[tileType];
    if (sprite) {
        drawSprite(ctx, sprite, x, y, scale);
    }
}
